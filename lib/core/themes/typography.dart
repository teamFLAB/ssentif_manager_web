class SsentifTextStyles extends AppTextStyles {
  /// 디바이스 크기에 따라 fontSize를 조정하는 헬퍼 메서드
  /// xlarge: 원래 크기, large: -2px, medium: -4px, small: -6px
  static double _getResponsiveFontSize(double baseSize, BuildContext? context) {
    if (context == null) {
      // context가 없으면 원래 크기 반환 (최대 화면 기준)
      return baseSize;
    }

    final category = DeviceSizeUtils().getScreenSizeCategory(context);
    switch (category) {
      case ScreenSizeCategory.xlarge:
        return baseSize; // 1440px 이상: 원래 크기
      case ScreenSizeCategory.large:
        return baseSize - 2; // 1024px ~ 1440px: -2px
      case ScreenSizeCategory.medium:
        return baseSize - 4; // 600px ~ 1024px: -4px
      case ScreenSizeCategory.small:
        return baseSize - 6; // 0 ~ 600px: -6px
    }
  }

